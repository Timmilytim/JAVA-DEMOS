package Collections;

import java.util.TreeSet;

public class TreeSetExample {
    public static void main(String[] args) {
        TreeSet<String> set = new TreeSet<>();
        set.add("Pineapple");
        set.add("Cup");
        set.add("Fiddle");
        set.add("Moon");
        set.add("Java");
        System.out.println(set);
    }
}
