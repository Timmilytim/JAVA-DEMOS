package Arrays;

public class DynamicArray {
    public static void main(String[] args) {
        String[] colors = new String[5];
        colors[0] = "blue";
        colors[1] = "yellow";
        colors[2] = "green";
        colors[3] = "purple";
        colors[4] = "red";

        System.out.println("Colors in the Array");
        for(String i : colors){
            System.out.println(i);
        }
    }
}
